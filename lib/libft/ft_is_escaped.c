#include "libft.h"

int	ft_is_escaped(char *s, int i)
{
	int	j;

	j = 0;
	while (i > 0 && s[i - 1] == '\\')
	{
		j++;
		i--;
	}
	if (j % 2 == 0)
		return (0);
	return (1);
}
